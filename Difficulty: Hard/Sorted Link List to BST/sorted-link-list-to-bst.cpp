class Solution{
  public:
    int findLenOfLL(LNode* head) {
        LNode* temp = head;
        int count = 0;
        while(temp != NULL) {
            count++;
            temp = temp->next;
        }
        return count;
    }
    
    TNode* makeBST(LNode*& head, int n) {
        if(n == 0 || head == NULL) return NULL;

        TNode* leftST = makeBST(head, n/2);

        TNode* root = new TNode(head->data);
        root->left = leftST;
        head = head->next;

        root->right = makeBST(head, n-n/2-1);

        return root;
    }
    
    TNode* sortedListToBST(LNode *head) {
        int len = findLenOfLL(head);
        return makeBST(head, len);
    }
};


