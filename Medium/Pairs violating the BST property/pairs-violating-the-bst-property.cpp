class Solution {
    static int k;

public:
    static int pairsViolatingBST(int n, Node* root) {
        k = 0;
        vector<int> arr(n, 0);
        inorder(root, arr);

        return countInversion(arr, 0, n - 1);
    }

    static void inorder(Node* root, vector<int>& arr) {
        if (root == nullptr) {
            return;
        }

        inorder(root->left, arr);
        arr[k++] = root->data;
        inorder(root->right, arr);
    }

    static int countInversion(vector<int>& arr, int l, int r) {
        int res = 0;
        if (l < r) {
            int m = l + (r - l) / 2;
            res += countInversion(arr, l, m);
            res += countInversion(arr, m + 1, r);
            res += countAndMerge(arr, l, m, r);
        }
        return res;
    }

    static int countAndMerge(vector<int>& arr, int l, int m, int r) {
        int n1 = m - l + 1;
        int n2 = r - m;
        vector<int> left(n1);
        vector<int> right(n2);

        for (int i = 0; i < n1; i++) {
            left[i] = arr[l + i];
        }
        for (int i = 0; i < n2; i++) {
            right[i] = arr[m + 1 + i];
        }

        int ans = 0, i = 0, j = 0, k = l;

        while (i < n1 && j < n2) {
            if (left[i] <= right[j]) {
                arr[k] = left[i];
                i++;
                k++;
            } else {
                arr[k] = right[j];
                j++;
                k++;
                ans += (n1 - i);
            }
        }

        while (i < n1) {
            arr[k] = left[i];
            i++;
            k++;
        }
        while (j < n2) {
            arr[k] = right[j];
            j++;
            k++;
        }

        return ans;
    }
};

